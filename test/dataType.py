"""
1.驗證數據類型
"""
# num1為int型  num2為float型
num1=1
num2=1.1
print(type(num1))
print(type(num2))

# a為str型
a="hello world"
print(type(a))

# b為bool型
b=True
print(type(b))

# c為list 列表型
c=[10,29,35]
print(type(c))

# d為tuple  元組型
d=(10,20,30)
print(type(d))

# set  集合型
e={10,20,30}
print(type(e))

# dict  字典型 鍵值對
f={'name':'Tom','age':16}
print(type(f))











